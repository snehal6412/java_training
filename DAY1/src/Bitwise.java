public class Bitwise 
{
    public static void main(String[] args)
    {
        
        int a = 3;
        int b = 2;
       
        System.out.println("Result is"+a+"&"+b+" = " + (a & b));
        System.out.println("Result is "+a+"|"+b+" = " + (a | b));
        System.out.println("Result is "+a+"^"+b+" = " + (a ^ b));
        System.out.println("Result is ~"+a+" = " + ~a);
      
        
    }
}

